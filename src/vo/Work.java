package vo;

public class Work {
	private int id;
	private String zhaoBiaoId;
	private String zhongBiaoId;
	private String workText;
	private String workReply;
	private int progress;
	private int workTime;
	private int workquality;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getZhaoBiaoId() {
		return zhaoBiaoId;
	}
	public void setZhaoBiaoId(String zhaoBiaoId) {
		this.zhaoBiaoId = zhaoBiaoId;
	}
	public String getZhongBiaoId() {
		return zhongBiaoId;
	}
	public void setZhongBiaoId(String zhongBiaoId) {
		this.zhongBiaoId = zhongBiaoId;
	}
	public String getWorkText() {
		return workText;
	}
	public void setWorkText(String workText) {
		this.workText = workText;
	}
	public String getWorkReply() {
		return workReply;
	}
	public void setWorkReply(String workReply) {
		this.workReply = workReply;
	}
	public int getProgress() {
		return progress;
	}
	public void setProgress(int progress) {
		this.progress = progress;
	}
	public int getWorkTime() {
		return workTime;
	}
	public void setWorkTime(int workTime) {
		this.workTime = workTime;
	}
	public int getWorkquality() {
		return workquality;
	}
	public void setWorkquality(int workquality) {
		this.workquality = workquality;
	}
	
}
